function setTopBar(sbarn) {
    x = 0;
    for (x = 0; x < 15; x++) {
        if (x <= sbarn) {
            topbar[x] = vino100_plugin_url + 'img/red_' + x + '.gif';
        }
        else {
            topbar[x] = vino100_plugin_url + 'img/white_' + x + '.gif';
        }
    }
    document.forms['post'].wine_flavor.value = sbarn;
    revTopBar();
}

